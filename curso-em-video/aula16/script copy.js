function soma(n1=0, n2=0) { //O =0 setados são para retornar algo se os parametros não estiverem setados lá embaixo no 2 e 6. 
    return n1 + n2
}

console.log(soma(2, 6)) //Aqui, não precisa declarar variavel, o 2 vai pro primeiro parametro e o 5 vai pro segundo parametro. n1 e n2 já são variaveis que vão abrigar os valores setados nos parametros.